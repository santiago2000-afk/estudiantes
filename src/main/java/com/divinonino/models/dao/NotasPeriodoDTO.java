package com.divinonino.models.dao;

public class NotasPeriodoDTO {

	private String materia;
    private Double notaGuias;
    private Double notaLaboratorio;
    private Double notaEvaluado;
    private Double promedioPeriodoMateria;
	public String getMateria() {
		return materia;
	}
	public void setMateria(String materia) {
		this.materia = materia;
	}
	public Double getNotaGuias() {
		return notaGuias;
	}
	public void setNotaGuias(Double notaGuias) {
		this.notaGuias = notaGuias;
	}
	public Double getNotaLaboratorio() {
		return notaLaboratorio;
	}
	public void setNotaLaboratorio(Double notaLaboratorio) {
		this.notaLaboratorio = notaLaboratorio;
	}
	public Double getNotaEvaluado() {
		return notaEvaluado;
	}
	public void setNotaEvaluado(Double notaEvaluado) {
		this.notaEvaluado = notaEvaluado;
	}
	public Double getPromedioPeriodoMateria() {
		return promedioPeriodoMateria;
	}
	public void setPromedioPeriodoMateria(Double promedioPeriodoMateria) {
		this.promedioPeriodoMateria = promedioPeriodoMateria;
	}
    
    
    
}
